int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int r;
	int t;

	r = 0;
	t = 0;
	while (s1[r] == s2[r] && r <= n)
	{
		r++;
	}
	if (s1[r] == s2[r])
		return (0);
	else
	{
		t = s1[r] - s2[r];
		return (t);
	}
}
