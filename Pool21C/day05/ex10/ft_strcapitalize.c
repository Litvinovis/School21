char	*ft_strcapitalize(char *str)
{
	int r;

	r = 1;
	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] -= 32;
	while (str[r] != '\0')
	{
		if (str[r] >= 'a' && str[r] <= 'z')
		{
			if ((str[r - 1] < 'a' || str[r - 1] > 'z') && (str[r - 1] < 'A' ||
				str[r - 1] > 'Z') && (str[r - 1] < '0' || str[r - 1] > '9'))
			{
				str[r] -= 32;
			}
		}
		r++;
	}
	return (str);
}
