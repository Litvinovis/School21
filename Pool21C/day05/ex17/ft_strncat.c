char	*ft_strncat(char *dest, char *src, int nb)
{
	int r;
	int a;

	a = 0;
	r = 0;
	while (dest[a] != '\0')
	{
		a++;
	}
	while (src[r] != '\0' && r < nb)
	{
		dest[a] = src[r];
		a++;
		r++;
	}
	dest[a] = '\0';
	return (dest);
}
