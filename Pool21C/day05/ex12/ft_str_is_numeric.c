int		ft_str_is_numeric(char *str)
{
	int r;

	r = 0;
	if (str[0] == '\0')
		return (1);
	while (str[r] != '\0')
	{
		if (str[r] < '0' || str[r] > '9')
			return (0);
		r++;
	}
	return (1);
}
