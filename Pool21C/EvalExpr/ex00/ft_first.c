#include "ft_list.h"

int		ft_first(char **str)
{
	int		c1;
	int		c2;
	char	o;

	c1 = ft_atoi(str);
	while ((*str)[0] != '\0' && (*str)[0] != ')')
	{
		o = (*str)[0];
		*str += 1;
		if (o == '+' || o == '-')
		{
			c2 = ft_calc(str);
		}
		else
		{
			c2 = ft_atoi(str);
		}
		c1 = ft_doop(c1, c2, o);
	}
	return (c1);
}
