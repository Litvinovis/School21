#include "ft_list.h"

int		ft_atoi(char **str)
{
	int	a;
	int b;

	a = 0;
	b = 1;
	if ((*str)[0] == '-' && (*str)[1] >= '0' && (*str)[1] <= '9')
	{
		b = -1;
		*str += 1;
	}
	if ((*str)[0] == '(')
	{
		*str += 1;
		a = ft_first(str);
		if ((*str)[0] == ')')
			*str += 1;
		return (a * b);
	}
	while ((*str)[0] >= '0' && (*str)[0] <= '9')
	{
		a = a * 10;
		a = a + (*str)[0] - '0';
		*str += 1;
	}
	return (a * b);
}
