int		ft_sqrt(int nb)
{
	int f;
	int d;
	int l;

	f = 1;
	d = 0;
	l = 1;
	while (d != nb && f < 46342)
	{
		d = f * f;
		f++;
		l++;
	}
	if (f == 46342)
	{
		return (0);
	}
	else
	{
		return (l - 1);
	}
}
