int		ft_antidote(int i, int j, int k)
{
	if ((i > j && i < k) || (i > k && i < j))
		return (i);
	if ((k > i && k < j) || (k > j && k < i))
		return (k);
	if ((j > i && j < k) || (j > k && j < i))
		return (j);
}
