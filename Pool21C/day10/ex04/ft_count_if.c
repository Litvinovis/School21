int		ft_count_if(char **tab, int (*f)(char*))
{
	int r;
	int t;

	r = 0;
	t = 0;
	while (tab[r] != '\0')
	{
		if (f(tab[r]) == 1)
		{
			t++;
		}
		r++;
	}
	return (t);
}
