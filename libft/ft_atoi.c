int		ft_atoi(const char *str)
{
	int a;
	int c;
	int b;

	a = 0;
	b = 1;
	c = 0;
	while (str[a] == ' ' || str[a] == '\n' || str[a] == '\t' ||
			str[a] == '\v' || str[a] == '\f' || str[a] == '\r')
		a++;
	if (str[a] == '+' && str[a + 1] >= '0' && str[a + 1] <= '9')
		a++;
	if (str[a] == '-' && str[a + 1] >= '0' && str[a + 1] <= '9')
	{
		b = -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
	{
		c = c * 10;
		c = c + (str[a] - '0');
		a++;
	}
	return (c * b);
}
